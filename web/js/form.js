if(!!document.getElementById("datepicker")){
    Calendar.setup({
        inputField:"datepicker",
        trigger:"datepicker",
        onSelect:function(){
            this.hide();
        },
        dateFormat:"%d/%m/%Y"
    });
}
Calendar.setup({
    cont:"ncalendar",
    onSelect:function(){
        this.hide();
    },
    dateFormat:"%d/%m/%Y"
});
setInterval(function(){
    var nYes = $("#nyes")[0].checked;
    if(nYes){
        $("#ncalendar").show();
    }
    else{
        $("#ncalendar").hide();
    }
},200);